def monkey_trouble(a_smile, b_smile):
    return (a_smile == b_smile)

monkey_trouble(True, True) #→ True
monkey_trouble(False, False) #→ True
monkey_trouble(True, False) #→ False
monkey_trouble(False, True) #→ False
